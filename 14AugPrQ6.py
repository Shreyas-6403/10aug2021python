#Tuple carrying 10 elements
My_tuple = (1,2,3,4,5,6,7,5,9,5)
print("Tuple",(My_tuple))

#Extract all list
print ("\n Extracted all list",My_tuple)

#Extract index number 2 to 5
print ("\n Extract number 2 to 5",My_tuple[2:5])

#Print list element reverse
print ("\n Reverse",My_tuple[::-1])

#use index
print("\n Using index", My_tuple.index(4))

#count
print("\n Using count",My_tuple.count(1))